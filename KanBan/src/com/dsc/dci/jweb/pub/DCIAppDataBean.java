package com.dsc.dci.jweb.pub;

import java.util.HashMap;

public class DCIAppDataBean {
	private String FrowardTagValue;
	private String DCITagValue;
	private String LangType;
	private String DefLangType;
	private String[] DBType;
	private HashMap<String,String> LangValues;
	private String UserID;
	private String CurrVersion;

	public String getFrowardTagValue() {
		return FrowardTagValue;
	}

	public void setFrowardTagValue(String frowardTagValue) {
		FrowardTagValue = frowardTagValue;
	}

	public String getDCITagValue() {
		return DCITagValue;
	}

	public void setDCITagValue(String dCITagValue) {
		DCITagValue = dCITagValue;
	}

	public String[] getDBType() {
		return DBType;
	}

	public void setDBType(String[] dBType) {
		DBType = dBType;
	}

	public HashMap<String,String> getLangValues() {
		return LangValues;
	}

	public void setLangValues(HashMap<String,String> langValues) {
		LangValues = langValues;
	}

	public String getLangType() {
		return LangType;
	}

	public void setLangType(String langType) {
		LangType = langType;
	}

	public String getDefLangType() {
		return DefLangType;
	}

	public void setDefLangType(String defLangType) {
		DefLangType = defLangType;
	}

	public String getUserID() {
		return UserID;
	}

	public void setUserID(String userID) {
		UserID = userID;
	}

	public String getCurrVersion() {
		return CurrVersion;
	}

	public void setCurrVersion(String currVersion) {
		CurrVersion = currVersion;
	}

}
